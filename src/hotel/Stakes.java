package hotel;

public class Stakes {
	private double s1;
	private double s2;
	private double s3;
	
	public Stakes(double s1, double s2, double s3) {
		this.s1 = s1;
		this.s2 = s2;
		this.s3 = s3;
	}
	
	public double getS1() {
		return s1;
	}
	public double getS2() {
		return s2;
	}
	public double getS3() {
		return s3;
	}
	
}
